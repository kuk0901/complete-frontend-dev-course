<template>
  <h1 @click="increase">
    {{ count }}
  </h1>
  <div v-if="count > 4">
    4보다 큽니다!
  </div>
  <ul>
    <MyFruit
      v-for="fruit in fruits"
      :key="fruit"
      :name="fruit">
      {{ fruit }}
    </MyFruit>
  </ul>
</template>

<script>
import MyFruit from "~/components/MyFruit";

export default {
  components: {
    MyFruit
  },
  data() {
    return {
      count: 0,
      fruits: ["Apple", "Banana", "Cherry"]
    }
  },
  methods: {
    increase() {
      this.count++;
    }
  }
}
</script>
